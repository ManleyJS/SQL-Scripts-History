create or replace view HAVEN_STAGING_AA_DEV.RM_STAGING.S0_BREAK_SEGMENTATION_LOOKUP_HISTORY(
	BREAK_DATE,
	BREAK_DAY,
	SHIP_CODE,
	BREAK_TYPE,
	SEGMENT_SEASON,
	SEGMENT_BREAK,
	SEGMENT_GROUP,
	TARGET_FILL_BREAK_SEGMENT
) as (
    WITH seg1 AS (
SELECT
     BREAKID::DATE as BREAK_DATE
    ,DAYNAME(BREAKID) as BREAK_DAY
    ,SHIP_CODE -- OCC CODE (DIFFERENT TO HAVEN CODE)
    ,BREAK_TYPE
    ,NULLIF(SPLIT_PART(TARGET_FILL_BREAK_SEGMENT, '|', 1),'') AS SEGMENT_SEASON
    ,NULLIF(SPLIT_PART(TARGET_FILL_BREAK_SEGMENT, '|', 2),'') AS SEGMENT_BREAK
    ,NULLIF(SPLIT_PART(TARGET_FILL_BREAK_SEGMENT, '|', 3),'') AS SEGMENT_GROUP
    ,TARGET_FILL_BREAK_SEGMENT
FROM
    HAVEN_STORE.pricer.break_segmentation_lookup_history a
INNER JOIN 
    HAVEN_STAGING_AA_DEV.RM_STAGING.s0_break_target_process_control_header pch 
    ON pch.break_segmentation_lookup_version_id = a.DATA_VERSION_ID
)
,seg2 as (
SELECT
     BREAKID::DATE as BREAK_DATE
    ,DAYNAME(BREAKID) as BREAK_DAY
    ,SHIP_CODE 
    ,BREAK_TYPE
    ,NULLIF(SPLIT_PART(TARGET_FILL_BREAK_SEGMENT, '|', 1),'') AS SEGMENT_SEASON
    ,NULLIF(SPLIT_PART(TARGET_FILL_BREAK_SEGMENT, '|', 2),'') AS SEGMENT_BREAK
    ,NULLIF(SPLIT_PART(TARGET_FILL_BREAK_SEGMENT, '|', 3),'') AS SEGMENT_GROUP
    ,TARGET_FILL_BREAK_SEGMENT
FROM
  HAVEN_MASTER.REVENUE_MANAGEMENT_STORAGE.break_segmentation_legacy_v1
WHERE 
    DATA_VERSION_ID = 1
    AND BREAK_DATE < (Select MIN(BREAK_DATE) FROM seg1)
)
, seg_union AS 
(
Select * from  seg1
UNION ALL 
Select * from  seg2
)
Select * 
FROM 
seg_union
  )
  ;


