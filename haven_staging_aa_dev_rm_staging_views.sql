create or replace view HAVEN_STAGING_AA_DEV.RM_STAGING.S0_PRORATED_BOOKING_REVENUE(
	BOOKING_ID,
	DELEGATE_RES_ID,
	UNIQUE_BK_ID,
	BOOKING_TYPE,
	BREAK_DATE,
	STAY_DATE,
	BOOKING_DATE,
	FIRM_DATE,
	CANCELLATION_DATE,
	ARRIVAL_DATE,
	DEPARTURE_DATE,
	BOOKING_STATUS,
	BOOKING_STATUS_CATEGORY,
	DURATION,
	PITCH_CODE,
	PRODUCT,
	SUB_PRODUCT,
	SOURCE,
	IN_SCOPE_FLAG,
	PARK_CODE,
	PARK_REGION,
	PAID_GRADE,
	REQUESTED_GRADE,
	ALLOCATED_GRADE,
	ADULTS,
	CHILDREN,
	INFANTS,
	PRORATED_NETT_BILLING_VALUE,
	DAY_RATIO,
	PRORATED_BROCHURE_PRICE,
	PRORATED_ACCOMMODATION_VALUE
) as (
    SELECT
    a.BOOKING_ID
    ,a.GROUP_ID AS DELEGATE_RES_ID
    ,CONCAT(a.BOOKING_ID, CASE WHEN a.GROUP_ID IS NULL then '' ELSE CONCAT('-', a.GROUP_ID) END) AS UNIQUE_BK_ID
    ,a.BOOKING_TYPE
    ,DATE(a.BREAK_DATE_XID, 'YYYYMMDD') AS BREAK_DATE
    ,DATE(a.STAY_DATE_XID, 'YYYYMMDD') AS STAY_DATE
    ,DATE(a.BOOKING_DATE_XID, 'YYYYMMDD') AS BOOKING_DATE
    ,DATE(a.BOOKING_DATE_XID, 'YYYYMMDD') AS FIRM_DATE
    ,DATE(NULLIF(a.CANCELLATION_DATE_XID,-1), 'YYYYMMDD') AS CANCELLATION_DATE
    ,DATE(a.ARRIVAL_DATE_XID, 'YYYYMMDD') AS ARRIVAL_DATE
    ,DATE(a.DEPARTURE_DATE_XID, 'YYYYMMDD') AS DEPARTURE_DATE
    ,b.BOOKING_STATUS_CODE AS BOOKING_STATUS
    ,b.BOOKING_STATUS_CATEGORY
    ,a.DURATION
    ,NULL AS PITCH_CODE
    ,f.PRODUCT_CODE AS PRODUCT
    ,f.SUB_PRODUCT_CODE AS SUB_PRODUCT
    ,g.BOOKING_SOURCE_CHANNEL_GROUP AS SOURCE
    ,CASE WHEN SOURCE = 'NEWSPAPER (SUN)' THEN FALSE ELSE TRUE END AS IN_SCOPE_FLAG
    ,d1.PARK_CODE
    ,d2.PARK_REGION
    ,CASE WHEN a.PAID_GRADE_XID='-1' THEN NULL ELSE e1.GRADE_CODE END AS PAID_GRADE
    ,CASE WHEN a.REQUESTED_GRADE_XID='-1' THEN NULL ELSE e2.GRADE_CODE END AS REQUESTED_GRADE
    ,CASE WHEN a.ALLOCATED_GRADE_XID='-1' THEN NULL ELSE e3.GRADE_CODE END AS ALLOCATED_GRADE
    ,a.ADULTS
    ,a.CHILDREN
    ,a.INFANTS
    ,COALESCE(NULLIF(a.PRORATED_NETT_BILLING_VALUE,0),a.PRORATED_DELEGATE_CHARGE_VALUE) AS PRORATED_NETT_BILLING_VALUE
    ,1 AS DAY_RATIO
    ,a.PRORATED_BROCHURE_PRICE
    ,a.PRORATED_ACCOMMODATION_VALUE
FROM
    HAVEN_STORE.holiday.fct_holiday_bookings_prorated a 

    INNER JOIN HAVEN_STORE.holiday.dim_booking_status b
    ON a.BOOKING_STATUS_XID = b.BOOKING_STATUS_XID

    INNER JOIN HAVEN_STORE.holiday.dim_booking_source_channel c
    ON a.BOOKING_SOURCE_CHANNEL_XID = c.BOOKING_SOURCE_CHANNEL_XID

    INNER JOIN HAVEN_STORE.common.dim_park d1
    ON a.PARK_XID = d1.PARK_XID

    LEFT JOIN HAVEN_STORE.common.parks d2
    ON d1.PARK_CODE = d2.PARK_CODE

    INNER JOIN HAVEN_STORE.holiday.dim_grade e1
    ON a.PAID_GRADE_XID = e1.GRADE_XID

    INNER JOIN HAVEN_STORE.holiday.dim_grade e2
    ON a.REQUESTED_GRADE_XID = e2.GRADE_XID

    INNER JOIN HAVEN_STORE.holiday.dim_grade e3
    ON a.ALLOCATED_GRADE_XID = e3.GRADE_XID

    INNER JOIN HAVEN_STORE.holiday.dim_package_type f
    ON a.PACKAGE_TYPE_XID = f.PACKAGE_TYPE_XID    

    INNER JOIN HAVEN_STORE.holiday.dim_booking_source_channel g
    ON a.BOOKING_SOURCE_CHANNEL_XID = g.BOOKING_SOURCE_CHANNEL_XID
    
WHERE
    a.SNAPSHOT_DATE = (SELECT MAX(SNAPSHOT_DATE) FROM HAVEN_STORE.holiday.fct_holiday_bookings_prorated)
    AND DATE(a.STAY_DATE_XID, 'YYYYMMDD') >= '2018-01-01'::DATE
    AND f.PRODUCT_CODE <> 'TOURING'
    AND b.BOOKING_STATUS_CATEGORY IN ('FIRM', 'CANCELLED')
  )
;

create or replace view HAVEN_STAGING_AA_DEV.RM_STAGING.S0_ROOM_TYPE_LOOKUP(
	ACCOM_CODE,
	PRODUCT_DESCRIPTION,
	GRADE_LEVEL_IND,
	ACCOM_DESCRIPTION,
	ACCOM_TYPES,
	ACCOM_GROUP,
	HAVEN_GRADE,
	LEVEL_1_GROUPING,
	LEVEL_2_GROUPING,
	PET_FRIENDLY,
	ROOM_SIZE
) as (
    WITH active AS (
SELECT
     ACCOM_CODE
    ,PRODUCT_DESCRIPTION
    ,CASE 
        WHEN PRODUCT_DESCRIPTION = 'Saver' THEN 0
        WHEN PRODUCT_DESCRIPTION = 'Bronze' THEN 1 
        WHEN PRODUCT_DESCRIPTION = 'Silver' THEN 2 
        WHEN PRODUCT_DESCRIPTION = 'Gold' THEN 3 
        WHEN PRODUCT_DESCRIPTION = 'Signature/Lodge' THEN 4
     END AS GRADE_LEVEL_IND
    ,ACCOM_DESCRIPTION
    ,ACCOM_TYPES
    ,ACCOM_GROUP
    ,HAVEN_GRADE
    ,LEVEL_1_GROUPING
    ,LEVEL_2_GROUPING
    ,CASE 
        WHEN ACCOM_DESCRIPTION LIKE '%Pet Friendly%' THEN 'Pet Friendly' 
        ELSE 'Standard' 
     END AS PET_FRIENDLY
    ,CASE 
        WHEN ACCOM_DESCRIPTION LIKE '%2 Bedroom%' THEN '2 Bedroom' 
        WHEN ACCOM_DESCRIPTION LIKE '%3 Bedroom%' THEN '3 Bedroom' 
        WHEN ACCOM_DESCRIPTION LIKE '%4 Bedroom%' THEN '4 Bedroom' 
        ELSE 'Other' 
     END AS ROOM_SIZE
FROM
    HAVEN_STORE.ocandc.room_type_lookup
WHERE
    VALID_TO_DT::DATE='0099-12-31'
    AND ACTIVE_ON_SEAWARE='Active'
    AND ACCOM_CODE IS NOT NULL
QUALIFY ROW_NUMBER() OVER(PARTITION BY ACCOM_CODE ORDER BY VALID_FROM_DT DESC) = 1
) 
SELECT * FROM active 
UNION ALL 
SELECT
     ACCOM_CODE
    ,PRODUCT_DESCRIPTION
    ,CASE 
        WHEN PRODUCT_DESCRIPTION = 'Saver' THEN 0
        WHEN PRODUCT_DESCRIPTION = 'Bronze' THEN 1 
        WHEN PRODUCT_DESCRIPTION = 'Silver' THEN 2 
        WHEN PRODUCT_DESCRIPTION = 'Gold' THEN 3 
        WHEN PRODUCT_DESCRIPTION = 'Signature/Lodge' THEN 4
     END AS GRADE_LEVEL_IND
    ,ACCOM_DESCRIPTION
    ,ACCOM_TYPES
    ,ACCOM_GROUP
    ,HAVEN_GRADE
    ,LEVEL_1_GROUPING
    ,LEVEL_2_GROUPING
    ,CASE 
        WHEN ACCOM_DESCRIPTION LIKE '%Pet Friendly%' THEN 'Pet Friendly' 
        ELSE 'Standard' 
     END AS PET_FRIENDLY
    ,CASE 
        WHEN ACCOM_DESCRIPTION LIKE '%2 Bedroom%' THEN '2 Bedroom' 
        WHEN ACCOM_DESCRIPTION LIKE '%3 Bedroom%' THEN '3 Bedroom' 
        WHEN ACCOM_DESCRIPTION LIKE '%4 Bedroom%' THEN '4 Bedroom' 
        ELSE 'Other' 
     END AS ROOM_SIZE
FROM
    HAVEN_STORE.ocandc.room_type_lookup 
WHERE
    ACTIVE_ON_SEAWARE<>'Active'
    AND ACCOM_CODE IS NOT NULL
    AND ACCOM_CODE NOT IN (SELECT DISTINCT ACCOM_CODE FROM active)
QUALIFY ROW_NUMBER() OVER(PARTITION BY ACCOM_CODE ORDER BY VALID_FROM_DT DESC) = 1
);

