create or replace view HAVEN_STAGING_AA_DEV.RM_STAGING.S0_BREAK_TARGET_PROCESS_CONTROL_HEADER(
	VERSION_ID,
	RUN_DATE,
	BREAK_SEGMENTATION_LOOKUP_VERSION_ID
