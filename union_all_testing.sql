create table dim_cust_a as select * from dim_customers;
create table dim_cust_b as select * from dim_customers;

select * from dim_customers;

delete from dim_cust_a where customer_id in (15, 45, 60);
delete from dim_cust_b where customer_id in (11, 33, 99);



SELECT CUSTOMER_ID, FIRST_NAME, LAST_NAME, FIRST_ORDER_DATE, MOST_RECENT_ORDER_DATE, NUMBER_OF_ORDERS 
FROM (
	SELECT CUSTOMER_ID, FIRST_NAME, LAST_NAME, FIRST_ORDER_DATE, MOST_RECENT_ORDER_DATE, NUMBER_OF_ORDERS
	FROM DIM_CUST_A
	UNION ALL
	SELECT CUSTOMER_ID, FIRST_NAME, LAST_NAME, FIRST_ORDER_DATE, MOST_RECENT_ORDER_DATE, NUMBER_OF_ORDERS
	FROM DIM_CUST_B
)
GROUP BY 1,2,3,4,5,6
HAVING count(*) = 1
ORDER BY 1,2,3,4,5,6
;

SELECT 'A', CUSTOMER_ID, FIRST_NAME, LAST_NAME, FIRST_ORDER_DATE, MOST_RECENT_ORDER_DATE, NUMBER_OF_ORDERS
