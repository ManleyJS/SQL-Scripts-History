WITH taba AS (
	SELECT GUEST_ID, GUEST_SEQN, OCCUPANCY_NUMBER, RES_ID, CLIENT_ID, GUEST_TYPE, AGE
	FROM (
		SELECT GUEST_ID, GUEST_SEQN, OCCUPANCY_NUMBER, RES_ID, CLIENT_ID, GUEST_TYPE, AGE
		FROM WARNER_RAW.SEAWARE.RES_GUEST
		UNION ALL 
		SELECT GUEST_ID, GUEST_SEQN, OCCUPANCY_NUMBER, RES_ID, CLIENT_ID, GUEST_TYPE, AGE
		FROM WARNER_RAW.SEAWARE_APS.RES_GUEST
		)
	GROUP BY 1,2,3,4,5,6,7
	HAVING count(*) = 1
	ORDER BY 1,2,3,4,5,6,7
	)
SELECT GUEST_ID, GUEST_SEQN, OCCUPANCY_NUMBER, RES_ID, CLIENT_ID, GUEST_TYPE, AGE
FROM (
	SELECT GUEST_ID, GUEST_SEQN, OCCUPANCY_NUMBER, RES_ID, CLIENT_ID, GUEST_TYPE, AGE
	FROM WARNER_RAW.SEAWARE.RES_GUEST
	UNION ALL 
	SELECT GUEST_ID, GUEST_SEQN, OCCUPANCY_NUMBER, RES_ID, CLIENT_ID, GUEST_TYPE, AGE
	FROM TABA
	)
GROUP BY 1,2,3,4,5,6,7
HAVING count(*) = 2
ORDER BY 4
;


