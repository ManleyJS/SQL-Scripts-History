USE ROLE sysadmin;

CREATE WAREHOUSE warner_account_information_warehouse_xsmall WITH WAREHOUSE_SIZE = 'XSMALL' WAREHOUSE_TYPE = 'STANDARD' 
AUTO_SUSPEND = 60 AUTO_RESUME = TRUE MIN_CLUSTER_COUNT = 1 MAX_CLUSTER_COUNT = 1 SCALING_POLICY = 'STANDARD';

use role useradmin;

create role _warner_account_information_warehouse_xsmall__operator;
create role _warner_account_information_warehouse_xsmall__usage;

-- Create a new role intended to monitor Snowflake usage.
create role warner_account_information;

USE ROLE securityadmin;

grant usage, monitor on warehouse warner_account_information_warehouse_xsmall to role _warner_account_information_warehouse_xsmall__usage;
grant role _warner_account_information_warehouse_xsmall__usage to role _warner_account_information_warehouse_xsmall__operator;
grant operate, modify on warehouse warner_account_information_warehouse_xsmall to role _warner_account_information_warehouse_xsmall__operator;

GRANT ROLE _warner_account_information_warehouse_xsmall__usage TO ROLE warner_account_information;
GRANT ROLE _WARNER_WORK__COMMON__READER TO ROLE  warner_account_information;

USE ROLE warner_account_information;

USE ROLE DBA;

