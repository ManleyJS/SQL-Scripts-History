USE ROLE securityadmin;

GRANT ROLE _WARNER_WORK__COMMON__READER TO ROLE WARNER_TERRAFORM_TRANSFORM_DEV;
GRANT ROLE _WARNER_WORK__COMMON__READER TO ROLE WARNER_DBT_TRANSFORM_DEV;

USE ROLE DBA;

USE SCHEMA WARNER_WORK.COMMON;

CREATE TASK WARNER_COPY_HISTORY
WAREHOUSE = WARNER_ACCOUNT_INFORMATION_WAREHOUSE_XSMALL
SCHEDULE = 'USING CRON 0 6 * * * UTC'
COMMENT = 'Task to populate COPY_HISTORY table for Warners'
AS
INSERT INTO WARNER_WORK.COMMON.COPY_HISTORY
SELECT FILE_NAME, STAGE_LOCATION, LAST_LOAD_TIME, ROW_COUNT, ROW_PARSED, FILE_SIZE, FIRST_ERROR_MESSAGE, FIRST_ERROR_LINE_NUMBER, FIRST_ERROR_CHARACTER_POS, FIRST_ERROR_COLUMN_NAME, ERROR_COUNT, ERROR_LIMIT, STATUS, TABLE_ID, TABLE_NAME, TABLE_SCHEMA_ID, TABLE_SCHEMA_NAME, TABLE_CATALOG_ID, TABLE_CATALOG_NAME, PIPE_CATALOG_NAME, PIPE_SCHEMA_NAME, PIPE_NAME, PIPE_RECEIVED_TIME, FIRST_COMMIT_TIME
FROM SNOWFLAKE.ACCOUNT_USAGE.COPY_HISTORY 
WHERE TABLE_CATALOG_name LIKE 'WARNER_%'
and LAST_LOAD_TIME > (SELECT MAX(LAST_LOAD_TIME) FROM WARNER_WORK.COMMON.COPY_HISTORY);

SHOW TASKS;

EXECUTE TASK WARNER_COPY_HISTORY;

